Cypress.Commands.add('clickIfVisible', (selector) => {
    cy.get(selector,{ timeout: 5000 }).then(($element) => {
      if ($element.is(':visible')) {
        cy.wrap($element).click({force:true});
      } else {
        cy.log(`Element with selector '${selector}' is not visible, skipping click.`);
      }
    });
  });
  